int	ft_isalnum(int arg)
{
	if ('0' <= arg && arg <= '9')
		return (1);
	if ('a' <= arg && arg <= 'z')
		return (1);
	if ('A' <= arg && arg <= 'Z')
		return (1);
	return (0);
}
